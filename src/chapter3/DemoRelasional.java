package chapter3;

public class DemoRelasional {
    public static void main(String[] args) {
        System.out.println("Demo Relasional");
        int a = 5, b = 10;

        System.out.println("a == b bernilai : " + (a == b));
        System.out.println("a != b bernilai : " + (a != b));
        System.out.println("a > b bernilai : " + (a > b));
        System.out.println("a < b bernilai : " + (a < b));
        System.out.println("a >= b bernilai : " + (a >= b));
        System.out.println("a <= b bernilai : " + (a <= b));
    }
}
