public class ArrayJava {

    public static void main(String[] args) {
        // Array
        System.out.println("Hello world");
        char []c={'a','b','c','d','e'};
        System.out.println(c);
        c[1]='S';
        System.out.println(c);
        System.out.println(c[0]);
        System.out.println(c[1]);
        System.out.println(c[2]);
        int i;
        for(i=0;i<=4;i++){
            System.out.println(c[i]);
        }
    }
}
