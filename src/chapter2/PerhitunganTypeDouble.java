package chapter2;

public class PerhitunganTypeDouble {
    public static void main(String[] args) {
        // Hitung Keliling lingkaran
        System.out.println("Hitung Keliling lingkaran");
        double pi = 3.1416;
        double r = 2.12;
        double keliling;
        keliling = 2 * pi * r;
        System.out.println("Luas Keliling lingkaran adalah : " + keliling);
    }
}
